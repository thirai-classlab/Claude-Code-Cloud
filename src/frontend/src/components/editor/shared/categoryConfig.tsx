/**
 * Normalize category to known categories or 'custom'
 */
export const normalizeCategory = (
  category: string,
  categoryOrder: EditorCategory[] = CATEGORY_ORDER
): EditorCategory => {
  if (categoryOrder.includes(category as EditorCategory)) {
    return category as EditorCategory;
  }
  return 'custom';
};
